package J6_ForLoop;

public class Patika_HarmonikSayi {
    public static void main(String[] args) {
        
        int n=2;
        double harmonikToplam=0;

        for (double i = 1; i <=n ; i++) {
            harmonikToplam+=1/i;
        }
        System.out.println(harmonikToplam);
        
        
        
        
    }
}
