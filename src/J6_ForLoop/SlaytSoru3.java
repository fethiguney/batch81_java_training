package J6_ForLoop;

public class SlaytSoru3 {
    public static void main(String[] args) {

        int sayi1=100;
        int sayi2=50;

        for (int i = sayi1; i >=sayi2 ; i--) {
            if (i>sayi2) {
                System.out.print(i+", ");
            } else System.out.print(i);
        }





    }
}
